    const od1 = new Odometer({
      el: document.getElementById("odometer1"),
      format: "(,ddd).dd",
      duration: 3000,
      theme: "default"
    });

    od1.render();
    console.log(96, $(window).scrollTop() > position)
    if ($(window).scrollTop() > position) {
      setTimeout(function() {
        od1.update(50);
      }, 100);
    } else {
      $(window).on('scroll', function(e) {
        //數字滾動開始
        if ($(window).scrollTop() > position) {
          // Initial Animation
          setTimeout(function() {
            od1.update(50);
          }, 100);

        }
      });

    }

    window.addEventListener('load', function(e) {

      let aboutSect = document.querySelector('.about-sect');
      window.addEventListener('scroll', function(e) {
        let asPosition = aboutSect.getBoundingClientRect()
